import java.util.Scanner;
public class hi  {
    public static void main(String[] args) {
        //   &一假全假
        System.out.println(true&false);
        System.out.println(true&true);
       //   |   一真全真
        System.out.println(true|true);
        System.out.println(true|false);
        //  ^  同为假，异为真
        System.out.println(true^true);
        System.out.println(true^false);
    }
}

